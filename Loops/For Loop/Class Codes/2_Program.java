/*
 Program 2 : Take N as input print odd integer 
 */

class printOdd{

        public static void main(String[] args){

                int N = 10;

                for(int i = 1;i <= N;i++){
			
			if(i % 2 != 0){
                        	System.out.println(i);
			}
                }
        }
}

