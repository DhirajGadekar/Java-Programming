//character value is use in Switch :

class SwitchDemo{
	
	public static void main(String[] args){
		
		int num = 65;

		switch(num){

			case 'A':
				System.out.println("A");
				break;
			case 66:
				System.out.println("B");
				break;
			default:
				System.out.println("Case No Match");
				break;

		}
	}
}
