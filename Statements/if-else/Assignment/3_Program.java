//Program 3:Write a java program take a number and print whether it is positive or negative


class NumPosNeg{

        public static void main(String[] args){

                int num = -1;

                if(num > 0){
                        System.out.println(num + " is Positive");
                }else if(num < 0){

                        System.out.println(num + " is Negative");
                }else{

                        System.out.println(num + " is Neutral");
                }
        }
}

